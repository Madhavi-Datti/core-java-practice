package com.operators;

public class UnaryOperator {

	public static void main(String[] args) {
		int a = 10;
		int b =20;
		int c = 30;
		boolean d = true;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
		System.out.println(~a);
		System.out.println(b++ + c++);
		System.out.println(b-- + --c);
		System.out.println(!d);
		

	}

}
